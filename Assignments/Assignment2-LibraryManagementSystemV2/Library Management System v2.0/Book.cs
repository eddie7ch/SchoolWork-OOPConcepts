namespace Library_Management_System_v2._0
{
    /// <summary>
    /// Represents a book in the library.
    /// Inherits from <see cref="Media"/> and implements <see cref="IBorrowable"/>.
    /// </summary>
    public class Book : Media, IBorrowable
    {
        // ?? Properties ???????????????????????????????????????????????????
        /// <summary>Author of the book.</summary>
        public string Author { get; set; }

        /// <summary>Genre / category of the book.</summary>
        public string Genre { get; set; }

        // ?? Constructor ??????????????????????????????????????????????????
        /// <summary>
        /// Initialises a new <see cref="Book"/> with the given details.
        /// </summary>
        public Book(string title, string author, string genre)
            : base(title)
        {
            Author = author;
            Genre  = genre;
        }

        // ?? IBorrowable ??????????????????????????????????????????????????
        /// <inheritdoc/>
        public void Borrow(Borrower borrower)
        {
            if (!IsAvailable)
                throw new InvalidOperationException($"'{Title}' is already borrowed by {CurrentBorrower?.Name}.");

            IsAvailable     = false;
            CurrentBorrower = borrower;
        }

        /// <inheritdoc/>
        public void Return()
        {
            if (IsAvailable)
                throw new InvalidOperationException($"'{Title}' is not currently borrowed.");

            IsAvailable     = true;
            CurrentBorrower = null;
        }

        // ?? Display ??????????????????????????????????????????????????????
        /// <inheritdoc/>
        public override string GetDetails()
        {
            string status = IsAvailable
                ? "Available"
                : $"Borrowed by {CurrentBorrower?.Name}";

            return $"[Book  #{MediaId}] \"{Title}\" — Author: {Author} | Genre: {Genre} | Status: {status}";
        }
    }
}
