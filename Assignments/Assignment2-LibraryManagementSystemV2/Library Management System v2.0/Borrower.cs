namespace Library_Management_System_v2._0
{
    /// <summary>
    /// Represents a library borrower with personal and contact details.
    /// </summary>
    public class Borrower
    {
        // ?? Auto-increment ID counter ?????????????????????????????????????
        private static int _nextId = 1;

        // ?? Properties ???????????????????????????????????????????????????
        /// <summary>Unique identifier for the borrower.</summary>
        public int BorrowerId { get; private set; }

        /// <summary>Full name of the borrower.</summary>
        public string Name { get; set; }

        /// <summary>Mailing address of the borrower.</summary>
        public string Address { get; set; }

        /// <summary>Contact phone number.</summary>
        public string ContactNumber { get; set; }

        /// <summary>Email address.</summary>
        public string Email { get; set; }

        // ?? Constructor ??????????????????????????????????????????????????
        /// <summary>
        /// Initialises a new <see cref="Borrower"/> and assigns a unique ID.
        /// </summary>
        public Borrower(string name, string address, string contactNumber, string email)
        {
            BorrowerId    = _nextId++;
            Name          = name;
            Address       = address;
            ContactNumber = contactNumber;
            Email         = email;
        }

        // ?? Display ??????????????????????????????????????????????????????
        /// <summary>Returns a formatted summary of the borrower's details.</summary>
        public override string ToString()
        {
            return $"[Borrower #{BorrowerId}] {Name} | Address: {Address} | Phone: {ContactNumber} | Email: {Email}";
        }
    }
}
