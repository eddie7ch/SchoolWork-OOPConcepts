namespace Library_Management_System_v2._0
{
    /// <summary>
    /// Represents a DVD in the library.
    /// Inherits from <see cref="Media"/> and implements <see cref="IBorrowable"/>.
    /// </summary>
    public class DVD : Media, IBorrowable
    {
        // ?? Properties ???????????????????????????????????????????????????
        /// <summary>Director of the DVD.</summary>
        public string Director { get; set; }

        /// <summary>Runtime of the DVD in minutes.</summary>
        public int RuntimeMinutes { get; set; }

        // ?? Constructor ??????????????????????????????????????????????????
        /// <summary>
        /// Initialises a new <see cref="DVD"/> with the given details.
        /// </summary>
        public DVD(string title, string director, int runtimeMinutes)
            : base(title)
        {
            Director       = director;
            RuntimeMinutes = runtimeMinutes;
        }

        // ?? IBorrowable ??????????????????????????????????????????????????
        /// <inheritdoc/>
        public void Borrow(Borrower borrower)
        {
            if (!IsAvailable)
                throw new InvalidOperationException($"'{Title}' is already borrowed by {CurrentBorrower?.Name}.");

            IsAvailable     = false;
            CurrentBorrower = borrower;
        }

        /// <inheritdoc/>
        public void Return()
        {
            if (IsAvailable)
                throw new InvalidOperationException($"'{Title}' is not currently borrowed.");

            IsAvailable     = true;
            CurrentBorrower = null;
        }

        // ?? Display ??????????????????????????????????????????????????????
        /// <inheritdoc/>
        public override string GetDetails()
        {
            string status = IsAvailable
                ? "Available"
                : $"Borrowed by {CurrentBorrower?.Name}";

            return $"[DVD   #{MediaId}] \"{Title}\" — Director: {Director} | Runtime: {RuntimeMinutes} min | Status: {status}";
        }
    }
}
